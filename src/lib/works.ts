const pick = <T,>(arr: T[], i: number, n: number): T[] =>
  Array.from({ length: n }, (_, k) => arr[(i * 3 + k * 2) % arr.length]);

const slugify = (b: string, m: string) =>
  (b + "-" + m).toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "");

